package week1.day1;

public class HelloWorld {
public static void main(String[] args) {
	System.out.println("Hello World");
	//Data Type Variable Name = Value;
	int inputOne = 5;
	System.out.println(inputOne);
	byte inputTwo = 127;
	System.out.println(inputTwo);
	short inputThree = -3589;
	System.out.println(inputThree);
	long inputFour = 98987987878787L;
	System.out.println(inputFour);
	
	float inputFive = 3456.231343f;
	System.out.println(inputFive);
	double inputSix = 2345.523;
	System.out.println(inputSix);
	
	char inputSeven = 'M';
	System.out.println(inputSeven);
	
	boolean inputEight = true;
	System.out.println(inputEight);
	boolean inputEightOne = false;
	System.out.println(inputEightOne);
	
	String name = "Selenium";
	System.out.println(name);
				
}
}
